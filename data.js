// Fungsi sorting
function sortBy(field) {
  const data = getData();
  const keyword = document.getElementById("search-input").value.toLowerCase().trim();

  let filtered = data.filter(item => {
    return (
      item.kode.toLowerCase().includes(keyword) ||
      item.underwriter.join(" ").toLowerCase().includes(keyword)
    );
  });

  // Toggle arah sorting
  if (currentSort.field === field) {
    currentSort.ascending = !currentSort.ascending;
  } else {
    currentSort.field = field;
    currentSort.ascending = false;
  }

  filtered.sort((a, b) => {
    const valA = a[field];
    const valB = b[field];
    return currentSort.ascending ? valA - valB : valB - valA;
  });

  tampilkanData("", filtered);
  updateSortIndicators();
}

// Tampilkan panah di kolom yang sedang disortir
function updateSortIndicators() {
  const fields = ["day1", "day7", "month1", "month6", "year1"];
  fields.forEach(field => {
    const el = document.getElementById("sort-" + field);
    if (!el) return;

    if (currentSort.field === field) {
      el.textContent = currentSort.ascending ? "↑" : "↓";
    } else {
      el.textContent = "";
    }
  });
}

// Tampilkan data ke tabel
function tampilkanData(filter = "", customData = null) {
  const data = customData || getData();
  const tbody = document.getElementById("ipo-table-body");
  tbody.innerHTML = "";

  const keyword = filter.toLowerCase().trim();

  const filtered = customData
    ? data
    : data.filter(item => {
        return (
          item.kode.toLowerCase().includes(keyword) ||
          item.underwriter.join(" ").toLowerCase().includes(keyword)
        );
      });

  filtered.forEach(item => {
    const row = document.createElement("tr");
    row.innerHTML = `
      <td>${item.tanggal}</td>
      <td>${item.kode}</td>
      <td>${item.perusahaan}</td>
      <td>${item.underwriter.join(", ")}</td>
      <td style="color: ${item.day1 >= 0 ? 'green' : 'red'}">${item.day1}%</td>
      <td style="color: ${item.day7 >= 0 ? 'green' : 'red'}">${item.day7}%</td>
      <td style="color: ${item.month1 >= 0 ? 'green' : 'red'}">${item.month1}%</td>
      <td style="color: ${item.month6 >= 0 ? 'green' : 'red'}">${item.month6}%</td>
      <td style="color: ${item.year1 >= 0 ? 'green' : 'red'}">${item.year1}%</td>
    `;
    tbody.appendChild(row);
  });
}

// Jalankan saat halaman dimuat
document.addEventListener("DOMContentLoaded", () => {
  const searchInput = document.getElementById("search-input");

  if (searchInput) {
    searchInput.addEventListener("input", function (e) {
      tampilkanData(e.target.value);
    });
  }

  tampilkanData();
});
